package Replit;

public class For_while {

	public static void main(String[] args) {

		/*
		 * 1'den 100'e kadar olan doğal sayıların toplamını bulan programı yazınız.
		 * 
		 * OutPut:
		 * 
		 * Sayilarin Toplami : 5050
		 */

		int num = 0;

		for (int i = 0; i <= 100; i++) {

			num += i;

		}
		System.out.println(num);

	}

}
